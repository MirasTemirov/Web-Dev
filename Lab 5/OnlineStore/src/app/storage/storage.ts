export function get_category_by_id(id: number): Category | undefined {
  for(let c of data){
    if(c.id == id)
      return c
  }

  return undefined
}

export function get_item_by_id(category_id: number, id: number): Item | undefined {
  const c = get_category_by_id(category_id)

  if(c){
    for(let i of c.items){
      if(i.id == id)
        return i
    }
  }

  return undefined
}

export function like(category_id: number, id: number){

  for(let i = 0; i < data.length; i++){
    if(category_id == data[i].id){
      let c = data[i];

      for(let j = 0; j < c.items.length; j++){
        if(id == c.items[j].id){
          c.items[j].likes++;
          data[i] = c
          return;
        }
      }
    }
  }


}
